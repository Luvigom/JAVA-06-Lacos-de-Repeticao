package lacosderepeticao;

public class LacosDeRepeticao {

	public static void main(String[] args) {

		//laços de repetição estrutura para()for
		//for(inicio, condição, contador)
		
		for(int num = 1; num < 5; num++) {
			System.out.println("Agora o num vale: " +num);
		}
		//System.out.println("Bom dia!");
		
		int i;
		for(i = 0; i < 15; i ++) {
			System.out.println("Agora o num vale: " +i);
		}
		System.out.println("O valor de i ainda existe? " +i);
		
		
	}

}
